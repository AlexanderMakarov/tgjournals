package com.aleksandrmakarov.journals.model;

/**
 * Defines the types of questions in a training session.
 */
public enum QuestionType {
	/**
	 * Pre-session questions answered before training begins. Typically about goals,
	 * expectations, or preparation.
	 */
	BEFORE,

	/**
	 * Post-session questions answered after training is complete. Typically about
	 * reflection, achievements, and lessons learned.
	 */
	AFTER
}
